#include "libft.h"

void	rainbow_back(int index)
{
	char	c;

	c = (index % 7 + 1) + 48;
	if (index == -1)
		write(1, "\033[40m", 5);
	else
	{
		write(1, "\033[4", 3);
		write(1, &c, 1);
		write(1, "m", 1);
	}
}
