#include "libft.h"

void	printab_fd(char **tab, int fd)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (tab[i])
	{
		while (tab[i][j])
			j++;
		write(fd, tab[i], j);
		write(fd, "\n", 1);
		j = 0;
		i++;
	}
}
