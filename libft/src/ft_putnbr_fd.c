#include "libft.h"

void	ft_putnbr_fd(int fd, int n)
{
	long	i;

	i = n;
	if (i < 0)
	{
		ft_putchar_fd(fd, '-');
		i = -i;
	}
	if (i > 9)
		ft_putnbr_fd(fd, i / 10);
	ft_putchar_fd(fd, i % 10 + 48);
}
