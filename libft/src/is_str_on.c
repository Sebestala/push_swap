#include "libft.h"

int		is_str_on(char *str1, char *str2)
{
	int		i;
	int		j;

	i = 0;
	if (!str1 || !str2)
		return (-1);
	while (str1[i])
	{
		j = 0;
		while (str1[i] == str2[j])
		{
			i++;
			j++;
			if (!str2[j])
				return (1);
		}
		i++;
	}
	return (0);
}
